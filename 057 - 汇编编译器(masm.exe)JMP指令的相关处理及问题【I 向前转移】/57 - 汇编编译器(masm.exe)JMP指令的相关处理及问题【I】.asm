; 57 - 汇编编译器(masm.exe)JMP指令的相关处理及问题【I 向前转移】
; =================================================================
; 如果disp在区间[-128, 127]
; =================================================================

assume cs:codesg

codesg segment
	s:	jmp s
		jmp short s
		jmp near ptr s
		jmp far ptr s
codesg ends

end s


