; 62 - 转移指令在内存中的jmp指令【段内转移】 
; =================================================================

; =================================================================
; 转移指令在内存中的jmp指令有两种格式

; 1. jmp word ptr 内存单元地址（段内转移）	--> 从内存单元地址处开始存放着一个字，是转移的目的《偏移地址》，内存单元地址可用寻址方式的任一格式输出
; 2. jmp dword ptr 内存单元地址（段间转移）	--> 从内存单元地址处开始存放两个字，高地址处的字是转移的目的段地址，低地址处是转移的目的偏移地址
		; (CS) = (内存单元地址 + 2)
		; (IP) = (内存单元地址)



; 比如下面的指令 

; =================================================================

assume cs:codesg, ds:datasg

datasg segment
		db 32 dup ('+')
datasg ends

codesg segment
start:		mov ax, datasg
		mov ds, ax	

		mov ax, 0123H
		mov ds:[0], ax
		jmp word ptr ds:[0]

		; 或者这样，但这句不会执行，因为不知道跳哪去了
		
		mov ax, 6789H
		mov [bx], ax
		jmp word ptr [bx]


codesg ends

end start

