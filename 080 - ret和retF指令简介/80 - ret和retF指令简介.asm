; 80 - ret和retF指令简介
; =================================================================
; ret - 用栈中的数据 - 修改 IP - 实现近转移
; retf - 用栈中的数据 - 修改 CS：IP - 实现远转移
;
; cpu 在执行 ret 指令时，进行下面两步的操作
;	(IP) = ((ss)*16 + (sp))		--> pop ip
;	sp = sp + 2
;

; cpu 在执行retf指令时，进行下面4步操作
;	(IP) = ((ss)*16 + (sp))
;	(sp) = (sp) + 2			--> pop ip
;	(CS) = ((ss)*16 + (sp))
;	(sp) = (sp) + 2			--> pop cs
; ===============================================================


