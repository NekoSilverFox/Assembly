; 38 - 利用除法指令计算 1001÷100
; =================================================================
; 除法操作：div

; 分析：
;	1001÷100
;		1001 - 被除数，小于2^8 可以单独存放在AX寄存器中
;		==> 这是一个8位的除法
;
; 结果：
;	商 - AL
;	余数 - AH
; =================================================================

assume cs:codesg

codesg segment
start:		mov ax, 1001

			;div 100 			; 不能简单的这样，必须告诉编译器除数是几位的！！！！！
			;div byte ptr 100 	; 不能这样！ byte/word ptr 后面跟的是段寄存器！！！！

			mov bl, 100
			div bl
codesg ends

end start