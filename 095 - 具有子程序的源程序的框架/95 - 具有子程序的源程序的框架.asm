; 95 - 具有子程序的源程序的框架
; =================================================================

assume cs:codesg

; =================================================================

codesg segment

; =================================================================
	main:	:
		:
		call sub1		; 调用子程序sub1
		:
		:
		mov ax, 4c00h
		int 21h
; =================================================================
	sub1:	:		; 子程序sub1开始
		:
		call sub2	; 调用子程序 sub2
		:
		:
		ret		; 子程序返回		
; =================================================================
	sub2:	:		; 子程序sub2开始
		:
		:
		ret		; 子程序返回		
; =================================================================

codesg ends

; =================================================================
end main

